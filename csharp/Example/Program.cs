// 네임스페이스: 클래스 이름의 중복제거한다
namespace Example
{
    // internal class: 내부클래스를 의미
    internal class Program
    {
        // static void Main(): 프로그램 시작점(Entry Point)
        static void Main(string[] args)
        {
            // Console.WriteLineL: 콘솔창에 문자열 출력 함수
            Console.WriteLine("Hello, World!");
        }
    }
}
